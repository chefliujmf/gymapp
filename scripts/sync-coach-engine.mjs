// Sync the coaching ENGINE (method/philosophy) from the cyclingcoach repo into
// server/coach-engine.md, which server.js injects into every coach prompt.
//
// ONE polyvalent engine: this file is the shared coaching METHOD. The specific
// athlete (sport, goals, constraints) comes from their per-user PROFILE, which is
// injected separately. So we sync the transferable method here — NOT the per-domain
// numeric modules (codex_coach/instructions_*.md) or the 131M knowledge base; those
// are reference material for a future on-demand coach_knowledge tool.
//
// Usage: node scripts/sync-coach-engine.mjs [path-to-cyclingcoach]
//   (defaults to $COACH_ENGINE_REPO or ~/dev/cyclingcoach)
import { readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { homedir } from 'node:os'
import { fileURLToPath } from 'node:url'

const engineRepo = process.argv[2] || process.env.COACH_ENGINE_REPO || join(homedir(), 'dev/cyclingcoach')
const SOURCES = ['instructions.md'] // the coaching philosophy/method (transferable); extend deliberately
const here = dirname(fileURLToPath(import.meta.url))
const out = join(here, '..', 'server', 'coach-engine.md')

let body = ''
for (const s of SOURCES) {
  body += `\n\n<!-- source: cyclingcoach/${s} -->\n` + readFileSync(join(engineRepo, s), 'utf8').trim() + '\n'
}
const header = `# Platyplus coaching engine (method)

GENERATED by scripts/sync-coach-engine.mjs from the cyclingcoach repo. Do not edit here —
edit the source in cyclingcoach and re-run the sync.

This is the shared coaching METHOD for the one polyvalent Platyplus engine. The specific
athlete — sport, goals, constraints — comes from their per-user profile, injected separately.
Where the text below names a specific athlete or assumes cycling, treat it as a worked example
and adapt the method to the athlete actually described in the profile.
`
writeFileSync(out, header + body)
console.log(`wrote ${out} — ${(header + body).length} chars from: ${SOURCES.join(', ')}`)

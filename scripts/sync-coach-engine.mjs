// Sync the coaching ENGINE (method) from the cyclingcoach repo into the Platyplus
// coach prompt artifacts, which server.js injects.
//
// ONE polyvalent engine, but NOT cycling-for-everyone. The deep CYCLING method is
// for cyclists/endurance athletes (e.g. JM); a future non-cycling user must NOT be
// force-fed bike-volume / FTP / outdoor-ride rules. So we sync THREE bundles and
// server.js gates two of them per the user's profile:
//
//   server/coach-engine.md          — GENERIC method (every user): fuel/recovery,
//                                      strength, health/overtraining, mental cues.
//   server/coach-engine-cycling.md  — CYCLING/endurance method (gated): the cycling
//                                      coaching philosophy + weekly-volume ramp gate,
//                                      FTP estimation, workout analysis, weather.
//   server/coach-engine-female.md   — female-athlete module (gated by sex).
//
// The athlete's specifics (sport, goals, FTP, constraints) come from their per-user
// PROFILE, injected separately. We do NOT sync the 131MB knowledge_base, research
// source_notes, or tool/CLI docs (e.g. instructions_intervals_icu.md = the maintainer
// publish path; the Platyplus coach acts via the platyplus MCP, not the intervals API).
//
// Usage: node scripts/sync-coach-engine.mjs [path-to-cyclingcoach]
import { readFileSync, writeFileSync } from 'node:fs'
import { assertNoAthleteNames } from './engine-name-gate.mjs' // #519/#520 — shared no-hardcoded-athlete gate
import { join, dirname, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
// FORKED 2026-06-24: Platyplus OWNS its coach engine. The source is now the in-repo
// `coach-engine-src/` (a copy of cyclingcoach at the fork), NOT ~/dev/cyclingcoach.
// Adapt the engine HERE; cyclingcoach is now an independent intervals-only coach.
const engineRepo = process.argv[2] || process.env.COACH_ENGINE_REPO || join(here, '..', 'coach-engine-src')

// GENERIC — applies to ANY athlete/sport.
const SHARED = [
  'codex_coach/instructions_nutrition_recovery.md', // fuel/hydration/supplement method
  'codex_coach/instructions_strength.md',           // gym prescription + load coordination
  'codex_coach/instructions_health_and_peaking.md', // illness/overtraining gates, taper
  'codex_coach/instructions_sports_psychology.md',  // mental cues
  'codex_coach/instructions_public_text.md',        // #256: title/description rules — center the workout, no private-life leaks (syncs to Strava)
  'codex_coach/coach_feedback_format.md',            // #290: the exact "Coach note" structure for the completed-workout review
  'codex_coach/coach_action_feedback.md',            // #291: read non-device-verifiable feedback (food/fuel/supps/mobility/mind)
  'codex_coach/weekly_checkin.md',                   // #291: the weekly check-in template + how to fold it in
]
// CYCLING/endurance — gated: only for cyclists/endurance athletes.
const CYCLING = [
  'instructions.md',                                // the cycling coaching philosophy + objective
  'codex_coach/instructions_weekly_planning.md',    // bike-volume ramp gate, cottage, multi-week
  'codex_coach/instructions_fitness_estimation.md', // FTP method, confidence, validation cadence
  'codex_coach/instructions_workout_analysis.md',   // power-execution scoring + next prescription
  'codex_coach/workout_analysis_template.md',        // #291: the completed-ride analysis template (verdict/execution/next + public-text update)
  'codex_coach/training_zones.md',                   // #291: canonical power/HR zone reference (%FTP) — one source of truth for intensity words
  'codex_coach/instructions_weather.md',            // outdoor/indoor ride conversion
]
// Female-athlete — gated by sex.
const FEMALE = ['codex_coach/instructions_female_athlete.md']

const HEADER = (what, note) => `# Platyplus coaching engine — ${what}

GENERATED by scripts/sync-coach-engine.mjs from the cyclingcoach repo. Do not edit here —
edit the source in cyclingcoach and re-run the sync.

${note}

The specific athlete (sport, goals, FTP, constraints) comes from their per-user profile,
injected separately. Where the text names a specific athlete, treat it as a worked example
and adapt to the athlete in the profile. Referenced sibling files (athlete_profile.md,
ftp_estimate.md, training_zones.md, …) are NOT available here — use the profile + sensible
defaults. You act through the platyplus tools, not the intervals API directly.

OUTPUT ROUTING on Platyplus (public vs private — follow instructions_public_text + coach_feedback_format):
- PUBLIC (syncs to Strava, visible to others): the activity TITLE + DESCRIPTION. Set them with the
  \`set_activity_text\` tool. Public-safe ONLY. TITLE: human + creative (a normal athlete's Strava title,
  e.g. "KOM on the Backroads"). DESCRIPTION: precise + SCIENTIFIC (name zones + the adaptation, no vibe
  words); NO em-dash in either (see "Writing workout & activity TEXT"). NEVER put score, health/pain,
  fatigue/recovery, feelings, or future-plan protection here.
- PRIVATE (the athlete's coach view + the intervals Notes/comment thread): your review. Save it with
  \`save_coach_review\` (pass activityId) — Platyplus auto-posts it to the intervals Notes in the
  "Coach note" format. Put score, mind, body/recovery, nutrition/supplements, and next HERE.
Where the source text says to edit an intervals field or run a CLI command directly, do the equivalent
through these Platyplus tools instead.

BE PROACTIVE WITH INSIGHTS (JM directive). You are a coach, not a data dump. In every review, plan, and
chat, volunteer the ONE insight/tip/next-step that most helps this athlete IMPROVE — the "so what",
not just the numbers. Tie it to their data (decoupling, VI, PRs, trend vs last time, readiness) and
their goal. Prescribe concretely (tempo, suggested weight, target watts, what to change next time). Keep
it crisp and non-obvious — one genuinely useful cue beats many generic ones.
`

function bundle(sources) {
  let body = ''
  for (const s of sources) body += `\n\n<!-- source: cyclingcoach/${s} -->\n## [${basename(s, '.md')}]\n` + readFileSync(join(engineRepo, s), 'utf8').trim() + '\n'
  return body
}
const out = (name) => join(here, '..', 'server', name)
const words = (p) => readFileSync(p, 'utf8').split(/\s+/).length

writeFileSync(out('coach-engine.md'), HEADER('generic method (all athletes)', 'Sport-agnostic coaching method — applies to every Platyplus user.') + bundle(SHARED))
writeFileSync(out('coach-engine-cycling.md'), HEADER('cycling/endurance method (gated)', 'Injected ONLY for cyclists/endurance athletes. Do NOT apply bike-volume / FTP / outdoor-ride rules to non-endurance athletes.') + bundle(CYCLING))
writeFileSync(out('coach-engine-female.md'), HEADER('female-athlete module (gated)', 'Injected ONLY for female athletes.') + bundle(FEMALE))

// #519/#520 — NO-HARDCODED-ATHLETE gate: fail if any athlete name leaked into a generated engine. Shared with
// the standalone build check (scripts/engine-name-gate.mjs) so CI catches a DIRECT edit too, not just a re-sync.
assertNoAthleteNames(join(here, '..', 'server'))
console.log('coach-engine gate: OK (no hardcoded athlete names in the shared engine)')

console.log(`coach-engine.md         ${words(out('coach-engine.md'))} words (${SHARED.length} files, generic)`)
console.log(`coach-engine-cycling.md ${words(out('coach-engine-cycling.md'))} words (${CYCLING.length} files, gated)`)
console.log(`coach-engine-female.md  ${words(out('coach-engine-female.md'))} words (gated)`)
